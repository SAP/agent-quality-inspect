def main() -> None:
    parser = argparse.ArgumentParser(
        description="Add experiment results to the leaderboard dashboard (v2).",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog=__doc__,
    )
    parser.add_argument(
        "--results-dir",
        metavar="PATH",
        help="Path to an experiment output folder containing aggregate_metrics_results.json",
    )
    parser.add_argument(
        "--delete",
        action="store_true",
        help="Interactively delete an entry from the leaderboard",
    )
    parser.add_argument(
        "--batch",
        metavar="PARENT_DIR",
        help="Recursively find and add all experiment folders under PARENT_DIR",
    )
    args = parser.parse_args()

    if args.delete:
        cmd_delete()
        return

    # Handle batch mode
    if args.batch:
        parent_dir = Path(args.batch)
        if not parent_dir.exists():
            print(f"[error] Directory does not exist: {parent_dir}")
            sys.exit(1)

        print(f"  Scanning {parent_dir} for experiment folders...")
        experiment_dirs = find_experiment_dirs(parent_dir)

        if not experiment_dirs:
            print(f"  [error] No folders with aggregate_metrics_results.json found under {parent_dir}")
            sys.exit(1)

        print(f"\n  Found {len(experiment_dirs)} experiment folder(s):")
        for i, exp_dir in enumerate(experiment_dirs, start=1):
            print(f"    {i}. {exp_dir.relative_to(parent_dir.parent)}")

        print()
        confirm = input(f"  Add all {len(experiment_dirs)} experiments? [y/N] ").strip().lower()
        if confirm not in ("y", "yes"):
            print("  Aborted.")
            sys.exit(0)

        # Select dataset once for all experiments
        leaderboard = load_leaderboard()
        dataset_key, display_name = choose_dataset(leaderboard)

        # Process each experiment
        added_count = 0
        failed_count = 0

        for i, results_dir in enumerate(experiment_dirs, start=1):
            print(f"\n{'='*60}")
            print(f"  Processing [{i}/{len(experiment_dirs)}]: {results_dir.name}")
            print('='*60)

            try:
                # Load metrics
                metrics = load_aggregate_metrics(results_dir)
                print_experiment_summary(metrics)

                # Build entry
                entry = build_entry(metrics, results_dir)
                folder_name = generate_folder_name(entry, dataset_key)
                print(f"\n  Folder name: {folder_name}")

                # Add to leaderboard
                if dataset_key not in leaderboard["datasets"]:
                    leaderboard["datasets"][dataset_key] = {"display_name": display_name, "entries": []}
                leaderboard["datasets"][dataset_key]["entries"].append(entry)

                # Check for error analysis
                has_error_analysis = (results_dir / "error_analysis.pkl").exists()

                # Generate detail pages
                try:
                    generate_detail_pages(
                        entry,
                        display_name,
                        dataset_key,
                        generate_error_pages=has_error_analysis
                    )
                    print(f"  ✓ Detail pages generated")
                except Exception as exc:
                    print(f"  [warn] Detail page generation failed: {exc}")

                added_count += 1
                print(f"  ✓ Added successfully")

            except Exception as exc:
                print(f"  [error] Failed to process: {exc}")
                failed_count += 1
                continue

        # Save everything once at the end
        save_leaderboard(leaderboard)
        embed_data_in_html(leaderboard)

        print(f"\n{'='*60}")
        print(f"  Batch complete: {added_count} added, {failed_count} failed")
        print(f"  Dataset: '{display_name}' (now {len(leaderboard['datasets'][dataset_key]['entries'])} entries)")
        print(f"  Leaderboard DB : {LEADERBOARD_FILE}")
        print(f"  Dashboard      : {HTML_FILE}")
        print()
        print("  Open the dashboard:")
        print(f"    open {HTML_FILE}")
        print()
        return

    # Resolve results directory (single mode)
    if args.results_dir:
        results_dir = Path(args.results_dir)
    else:
        raw = input("Path to experiment output folder: ").strip()
        results_dir = Path(raw)

    if not results_dir.exists():
        print(f"[error] Directory does not exist: {results_dir}")
        sys.exit(1)

    # Load & display experiment info
    metrics = load_aggregate_metrics(results_dir)
    print_experiment_summary(metrics)

    # Check for error_analysis.pkl
    has_error_analysis = (results_dir / "error_analysis.pkl").exists()
    if has_error_analysis:
        print("\n  [info] error_analysis.pkl found - will generate error analysis pages")

    # Ask user whether to proceed
    print()
    confirm = input("  Add these results to the leaderboard? [y/N] ").strip().lower()
    if confirm not in ("y", "yes"):
        print("  Aborted.")
        sys.exit(0)

    # Select / create dataset
    leaderboard = load_leaderboard()
    dataset_key, display_name = choose_dataset(leaderboard)

    # Build entry
    entry = build_entry(metrics, results_dir)

    # Show the folder name that will be created
    folder_name = generate_folder_name(entry, dataset_key)
    print(f"\n  Folder name: {folder_name}")

    if dataset_key not in leaderboard["datasets"]:
        leaderboard["datasets"][dataset_key] = {"display_name": display_name, "entries": []}
    leaderboard["datasets"][dataset_key]["entries"].append(entry)

    # Persist leaderboard DB + update embedded data in index.html
    save_leaderboard(leaderboard)
    embed_data_in_html(leaderboard)

    n = len(leaderboard["datasets"][dataset_key]["entries"])
    print()
    print(f"  Added to '{display_name}' (now {n} {'entry' if n == 1 else 'entries'}).")

    # Generate detail pages with new folder naming
    print()
    print("  Generating detail pages...")
    try:
        summary = generate_detail_pages(
            entry,
            display_name,
            dataset_key,
            generate_error_pages=has_error_analysis
        )
        print(f"  Detail pages  -> {summary.parent}")
    except Exception as exc:
        print(f"  [warn] Detail page generation failed: {exc}")
        import traceback
        traceback.print_exc()
        summary = None

    print()
    print(f"  Leaderboard DB : {LEADERBOARD_FILE}")
    print(f"  Dashboard      : {HTML_FILE}")
    if summary:
        print(f"  Entry report   : {summary}")
    print()
    print("  Open the dashboard:")
    print(f"    open {HTML_FILE}")
    print()


if __name__ == "__main__":
    main()
