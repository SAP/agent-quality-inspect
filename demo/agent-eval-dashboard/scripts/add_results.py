def find_experiment_dirs(parent_dir: Path) -> list[Path]:
    """Recursively find all directories containing aggregate_metrics_results.json."""
    results = []
    for path in parent_dir.rglob("aggregate_metrics_results.json"):
        results.append(path.parent)
    return sorted(results)


def cmd_delete() -> None:
    leaderboard = load_leaderboard()
    datasets = leaderboard["datasets"]

    # Collect all entries as a flat list for display
    all_entries: list = []  # (dataset_key, entry_index)
    for ds_key, ds in datasets.items():
        for i, e in enumerate(ds["entries"]):
            all_entries.append((ds_key, i))

    if not all_entries:
        print("  No entries in the leaderboard.")
        sys.exit(0)

    print("\n  Leaderboard entries:")
    for n, (ds_key, idx) in enumerate(all_entries, start=1):
        e = datasets[ds_key]["entries"][idx]
        ds_name = datasets[ds_key]["display_name"]
        auc = e.get("metrics", {}).get("auc_score", {}).get("avg_max")
        auc_str = f"{auc:.3f}" if auc is not None else "N/A"
        persona = e.get("user_proxy_persona") or "—"
        folder_name = generate_folder_name(e, ds_key)
        print(f"    {n}. [{ds_name}]  {e.get('agent_model', '?')}  "
              f"persona={persona}  AUC={auc_str}  id={e['id']}")
        print(f"       folder: {folder_name}")
    print()

    while True:
        raw = input(f"  Select entry to delete [1-{len(all_entries)}] (or q to quit): ").strip()
        if raw.lower() == "q":
            print("  Aborted.")
            sys.exit(0)
        try:
            choice = int(raw)
        except ValueError:
            print("  Please enter a number.")
            continue
        if 1 <= choice <= len(all_entries):
            break
        print(f"  Please enter a number between 1 and {len(all_entries)}.")

    ds_key, idx = all_entries[choice - 1]
    entry = datasets[ds_key]["entries"][idx]
    ds_name = datasets[ds_key]["display_name"]
    folder_name = generate_folder_name(entry, ds_key)

    print(f"\n  About to delete:")
    print(f"    Dataset : {ds_name}")
    print(f"    Model   : {entry.get('agent_model', '?')}")
    print(f"    ID      : {entry['id']}")
    print(f"    Folder  : {folder_name}")
    print(f"    Added   : {entry.get('added_at', '?')}")
    print()
    confirm = input("  Delete? [y/N] ").strip().lower()
    if confirm not in ("y", "yes"):
        print("  Aborted.")
        sys.exit(0)

    datasets[ds_key]["entries"].pop(idx)

    # Remove the dataset key entirely if it has no entries left
    if not datasets[ds_key]["entries"]:
        del datasets[ds_key]
        print(f"  Dataset '{ds_name}' removed (no entries remaining).")

    save_leaderboard(leaderboard)
    embed_data_in_html(leaderboard)
    print(f"  Entry {entry['id']} deleted and dashboard updated.")

    # Remove generated pages (both old and new format)
    details_dir_old = DETAILS_BASE / f"entry_{entry['id']}"
    details_dir_new = DETAILS_BASE / folder_name
    error_dir = ERROR_ANALYSIS_BASE / folder_name

    for d in [details_dir_old, details_dir_new, error_dir]:
        if d.exists():
            shutil.rmtree(d)
            print(f"  Removed: {d}")


