def _infer_persona(metrics: dict, results_dir: Path) -> str:
    """Return persona from JSON field if present, else infer from folder name."""
    persona = metrics.get("user_proxy_persona", "")
    if persona:
        return persona
    return ""


def build_entry(metrics: dict, results_dir: Path) -> dict:
    # Normalise summary: Tau2Bench puts n_samples at top-level, not inside summary
    summary = dict(metrics.get("summary") or {})
    if "n_samples" not in summary and "n_samples" in metrics:
        summary["n_samples"] = metrics["n_samples"]

    return {
        "id": uuid.uuid4().hex[:8],
        "added_at": datetime.now().isoformat(timespec="seconds"),
        "source_folder": str(results_dir.resolve()),
        "agent_model": metrics.get("agent_model", ""),
        "agent": metrics.get("agent", ""),
        "max_turns": metrics.get("max_turns", 0),
        "user_proxy_model": metrics.get("user_proxy_model", ""),
        "user_proxy_persona": _infer_persona(metrics, results_dir),
        "n_trials": metrics.get("n_trials", 0),
        "experiment_timestamp": metrics.get("timestamp", ""),
        "metrics": metrics.get("metrics", {}),
        "summary": summary,
    }


