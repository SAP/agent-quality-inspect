#!/usr/bin/env python3
"""
Add experiment results to the leaderboard dashboard.

Generates:
- Detail pages with readable folder names: {dataset}_{model}_{persona}_{id}
- Error analysis pages (if error_analysis.pkl exists)

Usage:
    # Single experiment
    python demo/agent-eval-dashboard/scripts/add_results.py --results-dir paper_experiments/experiment_outputs_20260220_014503
    python demo/agent-eval-dashboard/scripts/add_results.py          # prompts for path interactively

    # Batch mode - add all experiments under a parent directory
    python demo/agent-eval-dashboard/scripts/add_results.py --batch paper_experiments/
"""

import argparse
import json
import re
import shutil
import sys
import uuid
from datetime import datetime
from pathlib import Path

# Add scripts dir to path for imports
sys.path.insert(0, str(Path(__file__).parent))
from page_generators.generate_detail_pages import generate_detail_pages, generate_folder_name

REPO_ROOT = Path(__file__).parent.parent.parent.parent
DASHBOARD_ROOT = Path(__file__).parent.parent  
LEADERBOARD_FILE = DASHBOARD_ROOT / "leaderboard_data.json"
LEADERBOARD_JS = DASHBOARD_ROOT / "leaderboard" / "leaderboard_data.js"
HTML_FILE = DASHBOARD_ROOT / "leaderboard" / "index.html"
DETAILS_BASE = DASHBOARD_ROOT / "leaderboard" / "details"
ERROR_ANALYSIS_BASE = DASHBOARD_ROOT / "leaderboard" / "error_analysis"


# ---------------------------------------------------------------------------
# I/O helpers
# ---------------------------------------------------------------------------

def load_leaderboard() -> dict:
    if LEADERBOARD_FILE.exists():
        with open(LEADERBOARD_FILE, encoding="utf-8") as f:
            return json.load(f)
    return {"datasets": {}}


def save_leaderboard(data: dict) -> None:
    with open(LEADERBOARD_FILE, "w", encoding="utf-8") as f:
        json.dump(data, f, indent=2, ensure_ascii=False)


def _slim_entry(entry: dict) -> dict:
    """Return entry with per-sample metric data stripped (not needed by the leaderboard table)."""
    slim_metrics = {}
    for metric_key, metric_val in entry.get("metrics", {}).items():
        if not isinstance(metric_val, dict):
            slim_metrics[metric_key] = metric_val
            continue
        slim_metrics[metric_key] = {
            k: v for k, v in metric_val.items()
            if k not in ("max_per_sample", "per_sample")
        }
    return {**entry, "metrics": slim_metrics}


def write_leaderboard_js(data: dict) -> None:
    """Write slim (aggregate-only) leaderboard data to leaderboard_data.js."""
    slim = {
        "datasets": {
            ds_key: {
                "display_name": ds["display_name"],
                "entries": [_slim_entry(e) for e in ds.get("entries", [])],
            }
            for ds_key, ds in data["datasets"].items()
        }
    }
    payload = json.dumps(slim, indent=2, ensure_ascii=False)
    LEADERBOARD_JS.write_text(
        f"// Auto-generated by add_results.py — do not edit manually\n"
        f"window.LEADERBOARD_DATA = {payload};\n",
        encoding="utf-8",
    )


def embed_data_in_html(data: dict) -> None:
    write_leaderboard_js(data)


def load_aggregate_metrics(results_dir: Path) -> dict:
    metrics_file = results_dir / "aggregate_metrics_results.json"
    if not metrics_file.exists():
        print(f"[error] aggregate_metrics_results.json not found in:\n  {results_dir}")
        sys.exit(1)
    with open(metrics_file, encoding="utf-8") as f:
        return json.load(f)


