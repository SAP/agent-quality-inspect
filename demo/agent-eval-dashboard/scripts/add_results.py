def fmt(val, decimals: int = 3) -> str:
    if val is None:
        return "N/A"
    try:
        return f"{float(val):.{decimals}f}"
    except (TypeError, ValueError):
        return str(val)


def _n_samples(metrics: dict) -> str:
    """n_samples lives at top-level in Tau2Bench, inside summary in ShoppingBench."""
    s = metrics.get("summary", {})
    v = s.get("n_samples") if s else None
    return str(v) if v is not None else str(metrics.get("n_samples", "N/A"))


def print_experiment_summary(metrics: dict) -> None:
    m = metrics.get("metrics", {})
    s = metrics.get("summary", {}) or {}
    persona = metrics.get("user_proxy_persona") or "N/A"
    print("\n" + "=" * 55)
    print("  Experiment Summary")
    print("=" * 55)
    print(f"  Agent session  : {metrics.get('agent', 'N/A')}")
    print(f"  Model          : {metrics.get('agent_model', 'N/A')}")
    print(f"  User proxy     : {metrics.get('user_proxy_model', 'N/A')} ({persona})")
    print(f"  Max turns      : {metrics.get('max_turns', 'N/A')}")
    print(f"  Trials         : {metrics.get('n_trials', 'N/A')}")
    print(f"  Experiment time: {metrics.get('timestamp', 'N/A')}")
    print(f"  Samples        : {_n_samples(metrics)}  |  "
          f"Successful: {s.get('successful', 'N/A')}  |  Failed: {s.get('failed', 'N/A')}")
    print(f"  Avg turns/run  : {fmt(s.get('avg_turns'))}")
    print()
    print("  Metrics:")
    if "pass@k" in m:
        k = m["pass@k"].get("k", "?")
        print(f"    Pass@{k}       : {fmt(m['pass@k'].get('avg'))}")
    if "pass^k" in m:
        k = m["pass^k"].get("k", "?")
        print(f"    Pass^{k}       : {fmt(m['pass^k'].get('avg'))}")
    if "auc_score" in m:
        print(f"    AUC Max@k    : {fmt(m['auc_score'].get('avg_max'))}")
    if "ppt_score" in m:
        print(f"    PPT Max@k    : {fmt(m['ppt_score'].get('avg_max'))}")
    print("=" * 55)


