def slugify(name: str) -> str:
    slug = name.lower()
    slug = re.sub(r"[^\w\s-]", "", slug)
    slug = re.sub(r"[\s-]+", "_", slug)
    slug = re.sub(r"_+", "_", slug).strip("_")
    return slug


def choose_dataset(leaderboard: dict) -> tuple:
    """Interactively ask user to pick an existing dataset or create a new one.

    Returns (dataset_key, display_name).
    """
    datasets = leaderboard["datasets"]
    keys = list(datasets.keys())

    print("\n  Available datasets:")
    if keys:
        for i, key in enumerate(keys, start=1):
            count = len(datasets[key]["entries"])
            name = datasets[key]["display_name"]
            print(f"    {i}. {name}  ({count} {'entry' if count == 1 else 'entries'})")
    else:
        print("    (no datasets yet)")
    new_idx = len(keys) + 1
    print(f"    {new_idx}. Create a new dataset")
    print()

    while True:
        raw = input(f"  Select [1-{new_idx}]: ").strip()
        if not raw:
            continue
        try:
            choice = int(raw)
        except ValueError:
            print("  Please enter a number.")
            continue

        if 1 <= choice <= len(keys):
            key = keys[choice - 1]
            return key, datasets[key]["display_name"]
        if choice == new_idx:
            while True:
                name = input("  New dataset display name (e.g. 'Tau2Bench – Airline'): ").strip()
                if name:
                    break
                print("  Name cannot be empty.")
            key = slugify(name)
            # Avoid slug collision
            if key in datasets:
                key = f"{key}_{uuid.uuid4().hex[:4]}"
            return key, name
        print(f"  Please enter a number between 1 and {new_idx}.")


